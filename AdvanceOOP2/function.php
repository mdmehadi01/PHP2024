<?php

class Calculation
{
    function add($a, $b)
    {
        echo "Summation =" . ($a + $b) . "<br>";
    }
    function sub($a, $b)
    {
        echo "subtraction =" . ($a - $b) . "<br>";
    }
    function multi($a, $b)
    {
        echo "Multiplication =" . ($a * $b) . "<br>";
    }
    function dev($a, $b)
    {
        echo "Division =" . ($a / $b) . "<br>";
    }
}
